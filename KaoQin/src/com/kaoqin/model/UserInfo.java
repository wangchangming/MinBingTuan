package com.kaoqin.model;

public class UserInfo {

    private int id;
    private String userName;
    private String realName;
    private String password;
    private String mobile;
    private String email;
    private String pwd;
    private String birthday;
    private int groupId;
    private String groupName;
    private String sex;
    private String showCustomMobile;
    private String showMemberMobile;
    private String idNumber;
    private String status;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getUserName() {
        return userName;
    }
    public void setUserName(String userName) {
        this.userName = userName;
    }
    public String getRealName() {
        return realName;
    }
    public void setRealName(String realName) {
        this.realName = realName;
    }
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }
    public String getMobile() {
        return mobile;
    }
    public void setMobile(String mobile) {
        this.mobile = mobile;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public String getPwd() {
        return pwd;
    }
    public void setPwd(String pwd) {
        this.pwd = pwd;
    }
    public String getBirthday() {
        return birthday;
    }
    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }
    public int getGroupId() {
        return groupId;
    }
    public void setGroupId(int groupId) {
        this.groupId = groupId;
    }
    public String getGroupName() {
        return groupName;
    }
    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }
    public String getSex() {
        return sex;
    }
    public void setSex(String sex) {
        this.sex = sex;
    }
    public String getShowCustomMobile() {
        return showCustomMobile;
    }
    public void setShowCustomMobile(String showCustomMobile) {
        this.showCustomMobile = showCustomMobile;
    }
    public String getShowMemberMobile() {
        return showMemberMobile;
    }
    public void setShowMemberMobile(String showMemberMobile) {
        this.showMemberMobile = showMemberMobile;
    }
    public String getIdNumber() {
        return idNumber;
    }
    public void setIdNumber(String idNumber) {
        this.idNumber = idNumber;
    }
    public String getStatus() {
        return status;
    }
    public void setStatus(String status) {
        this.status = status;
    }
}
